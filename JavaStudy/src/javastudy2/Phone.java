package javastudy2;

public class Phone {

	private String name;
	private String tel;
	//필드 (멤버변수)
	public Phone(String name, String tel) {
		super();
		this.name = name;
		this.tel = tel;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}

	
	

	
	
}
